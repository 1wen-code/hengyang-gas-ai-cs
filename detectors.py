    "收费标准", "怎么收费",
    # 材料/证件
    "材料", "证件", "需要什么", "准备什么", "带什么",
    "身份证", "房产证", "户口本",
    # 维修/故障
    "维修", "报修", "修理", "上门", "修",
    "打不着火", "打不燃", "点不着", "不出热水",
    "火焰.*小", "红火", "黄火", "熄火",
    # 业务地点/时间
    "营业厅", "在哪里", "去哪里", "地址", "网点",
    "多久", "多长时间", "几个工作日",
    # 安检/停气
    "安检", "检查", "停气", "恢复供气",
