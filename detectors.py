def detect_faq(message: str) -> bool:
    """检测是否包含业务关键词，适合走FAQ检索"""
    for kw in FAQ_KEYWORDS:
        if re.search(kw, message):
            return True
    return False


# ═══════════════════════════════════════════════
# 5. NORMAL — 燃气相关但不是FAQ/危险
# ═══════════════════════════════════════════════

IRRELEVANT = [
    "写代码", "编程", "python", "java", "代码",
    "股票", "基金", "理财", "比特币",
    "游戏", "王者荣耀", "吃鸡",
    "政治", "选举", "明星", "电影",
    "数学", "计算", "方程式",
    "做饭", "菜谱", "食谱",
]


def detect_normal(message: str) -> bool:
    """检测是否可以走 normal 模式（燃气相关，不闲聊，不危险）"""
    for kw in IRRELEVANT:
        if kw in message.lower():
            return False
    return True
